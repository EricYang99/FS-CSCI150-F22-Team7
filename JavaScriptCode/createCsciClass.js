let nameArr = [];
let unitArr = [];
let offerArr = [];
let additionalInfoArr = [];
for(let i = 0; i < data.length; i++){ //We can do either sytle -> .name or ["name"]
	nameArr.push(data[i]["Names"]);
	unitArr.push(data[i]["Units"]);
	offerArr.push(data[i]["Usually Offered In"]);
	additionalInfoArr.push(data[i]["Additional Information \r"]);
};

//Return all the class data
function getClassInfo(){
	return data;
}
//Return a single instance of each class
function getClassName(index){
	return nameArr[index];
}
function getClassUnits(index){
	return unitArr[index];
}
function getClassOffered(index){
	return offerArr[index];
}
function getClassAdditionalInfo(index){
	return additionalInfoArr[index];
}
//Return all the info on one class
function getOneClass(index){
	let singleClass = [getClassName(index), getClassUnits(index), getClassOffered(index), getClassAdditionalInfo(index)];
	return singleClass;
}

function createDivBody(index){
    let docBod = document.getElementById("container");

	let singleClass = getOneClass(index);

	let dataList = document.createElement('DIV')

	let className = document.createElement('H3');
	className.appendChild(document.createTextNode(singleClass[0]));

	let unit = document.createElement('P');
	unit.appendChild(document.createTextNode(singleClass[1]));

	if(!singleClass[2] && (!singleClass[3] || singleClass[3] == "\r")){
		dataList.appendChild(className);
		dataList.appendChild(unit);
	}
	else if(!singleClass[2] && singleClass[3]){
		let moreInfo = document.createElement('P');
		moreInfo.appendChild(document.createTextNode(singleClass[3]));

		dataList.appendChild(className);
		dataList.appendChild(unit);
		dataList.appendChild(moreInfo);
	}
	else if(singleClass[3] == "\r" && singleClass[2]){
		let offered = document.createElement('P');
		offered.appendChild(document.createTextNode(singleClass[2]));

		dataList.appendChild(className);
		dataList.appendChild(unit);
		dataList.appendChild(offered);
	}
	else{
		let offered = document.createElement('P');
		offered.appendChild(document.createTextNode(singleClass[2]));
		let moreInfo = document.createElement('P');
		moreInfo.appendChild(document.createTextNode(singleClass[3]));

		dataList.appendChild(className);
		dataList.appendChild(unit);
		dataList.appendChild(offered);
		dataList.appendChild(moreInfo);
	}
	docBod.appendChild(dataList);
}